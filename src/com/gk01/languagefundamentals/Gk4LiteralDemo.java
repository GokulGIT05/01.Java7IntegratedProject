package com.gk01.languagefundamentals;

public class Gk4LiteralDemo {

	public static void main(String[] args) {
		
		// Usage of underscore
		int i = 10_5;
		System.out.println(i);
		
		// Binary form
		int i2 = 0b1111;
		System.out.println(12);
		
		int _$1A = 10;
		System.out.println(_$1A);
		
		int _$1AB = 10_10_5;
		System.out.println(_$1AB);
	}
}
